package com.example.animtest.raphael;

public class StringItem {

	private String id;
	private String stringItem;
	
	public StringItem(String id, String stringItem){
		this.id = id;
		this.stringItem = stringItem;
	}
	
	public String getId(){
		return id;
	}
	
	public String getStringItem(){
		return stringItem;
	}
}
